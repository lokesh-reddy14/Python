'''4.Conversion of one unit to another (such as hours to minutes, miles to km and etc)'''

print('Conversion of hours into minutes')
hour=int(input('enter the hours:'))
mins=hour*60
print("the minutes for the given hours:",mins)
print('Conversion of miles into kms')
mile=int(input('enter the miles:'))
km=mile*1.60934
print("the miles for the given km:",km)
print('Conversion of kgs into grams')
kg=int(input('enter the kgs:'))
gram=kg*1000
print("the kgs for the given grams:",gram)