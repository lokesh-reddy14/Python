'''5.Usage of mathematical functions in python like math.ceil, floor, fabs, fmod, trunc, 
pow, sqrt etc.'''

import math
x=float(input('enter the number1:'))
y=float(input('enter the number2:'))
print(math.ceil(x))
print(math.ceil(y))
print(math.floor(x))
print(math.floor(y))
print(math.fabs(x))
print(math.fabs(y))
print(math.fmod(x,y))
print(math.trunc(x))
print(math.trunc(y))
print(math.pow(x,y))
print(math.sqrt(x))
print(math.sqrt(y))